const norm = (s) => String(s ?? "").toLowerCase();

// 트렌드 제목 → 편집 카테고리(가장 많이 매칭되는 것). 없으면 '라이프'.
export function classifyEditorialCategory(title = "") {
  const t = norm(title);
  let best = "라이프", bestScore = 0;
  for (const [cat, kws] of Object.entries(CLASSIFY_KEYWORDS)) {
    const score = kws.reduce((n, k) => n + (t.includes(norm(k)) ? 1 : 0), 0);
    if (score > bestScore) { bestScore = score; best = cat; }
  }
  return best;
}

