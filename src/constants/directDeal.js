// 직거래 유도 감지 키워드 + 경고 문구
//   채팅 메시지 전송 시 키워드 포함 여부를 검사한다.
//   마스킹하지 않음 — 메시지 원문은 증거로 보존.
// ─────────────────────────────────────────────────────

export const DIRECT_DEAL_KEYWORDS = [
  "따로 연락", "개인 연락", "직접 연락",
  "현금으로", "현금 결제", "현금 할인",
  "플랫폼 말고", "앱 말고", "여기 말고",
  "계좌로 보내", "계좌이체", "통장으로",
  "카카오로", "문자로", "전화로 해요",
  "수수료 아깝", "수수료 빼고",
  "직접 계약", "따로 계약", "사이트 말고",
];

// 채팅창 경고 배너 문구
export const DIRECT_DEAL_WARNING =
  "⚠️ 직거래 유도는 공간마켓 이용약관 위반입니다.\n이 대화는 기록되며, 적발 시 계정이 정지될 수 있습니다.";

// 메시지에서 감지된 키워드 배열 반환 (없으면 빈 배열)
export function detectDirectDealKeywords(text) {
  if (!text || typeof text !== "string") return [];
  return DIRECT_DEAL_KEYWORDS.filter((kw) => text.includes(kw));
}
