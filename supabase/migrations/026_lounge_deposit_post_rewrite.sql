 where is_seed = true
   and title = E'업체는 보증금을 내는데 고객은 내지 않는 이유';

-- 3) 해당 글 시드 댓글 교체 (고객 관점 · 사람냄새). 실사용자 댓글(user_id 있음)은 보존.
delete from public.lounge_comments c
 using public.lounge_posts p
 where c.post_id = p.id
   and p.is_seed = true
   and p.title = E'업체는 보증금을 내는데 고객은 내지 않는 이유'
   and c.user_id is null;

with seed_c(nick, content, hrs) as (
  select * from (values
    (E'오해풀림',   E'고객은 보증금 안 낸다는 거 보고 오해가 풀렸어요. 저는 내야 하는 줄 알았네요.', 60),
    (E'검증된업체', E'업체가 보증금 내는 거였군요. 그래서 아무나 못 들어오는 거구나 싶네요.', 47),
    (E'신뢰중요',   E'검증된 업체만 활동한다니 좀 더 믿음이 가네요.', 33),
    (E'수익아님',   E'보증금이 공간마켓 수익이 아니라는 점이 인상적이에요.', 21),
    (E'이해완료',   E'안전결제랑 보증금이 다른 거였군요. 이제 확실히 이해됐어요.', 12)
  ) as v(nick, content, hrs)
)
insert into public.lounge_comments (post_id, user_id, anonymous_nickname, content, is_expert_reply, created_at)
select p.id, null, sc.nick, sc.content, false, now() - (sc.hrs || ' hours')::interval
from seed_c sc
join public.lounge_posts p
  on p.is_seed = true and p.title = E'업체는 보증금을 내는데 고객은 내지 않는 이유';

-- 4) comment_count 정합 (트리거가 있으면 자동이나 안전하게 백필)
update public.lounge_posts p
   set comment_count = (
     select count(*) from public.lounge_comments c
      where c.post_id = p.id
        and coalesce(c.is_deleted,false)=false
        and coalesce(c.is_hidden,false)=false
   )
 where p.is_seed = true
   and p.title = E'업체는 보증금을 내는데 고객은 내지 않는 이유';
