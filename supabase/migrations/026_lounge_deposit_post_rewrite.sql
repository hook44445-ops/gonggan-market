set search_path = public, extensions;

-- 1) 제목 교체 (기존 제목 후보 모두 매칭)
update public.lounge_posts
   set title = E'업체는 보증금을 내는데 고객은 내지 않는 이유'
 where is_seed = true
   and title in (
     E'보증금이 왜 필요한가요',
     E'보증금, 수수료인 줄 알았는데 돌려받는 돈입니다'
   );

-- 2) 본문 전체 교체 (고객 신뢰 관점 · 2,500~4,000자 · H2/H3 + FAQ)
