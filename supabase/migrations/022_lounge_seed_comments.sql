--
-- 목적:
--   · seed 운영글 카드의 댓글 수가 실제 댓글과 일치하도록 주제 맞춤 댓글 생성.
--   · 조회수 원자적 증가 RPC(increment_lounge_view) 제공.
--   · comment_count 를 실제 댓글 수와 자동 동기화하는 트리거(이후 일반 글 포함).
--
-- 기준:
--   · 글별 댓글 4~5개 · 주제 일치 · 광고/홍보/전화/카톡 유도 없음.
--   · 짧고 현실적인 커뮤니티 말투 · 운영자(공간마켓)+일반 사용자 혼합.
--   · is_deleted=false · is_hidden=false 기본값 → 노출 필터 통과.
--
-- 멱등: (post_id, content) 중복이면 재실행해도 추가 안 됨. 추가 전용.
-- 선행: 019/020/021 (운영글 20개 존재) 실행 후 실행 권장.
