insert into public.lounge_comments (post_id, user_id, anonymous_nickname, content, is_expert_reply, created_at)
select p.id, null, sc.nick, sc.content, false, now() - (sc.hrs || ' hours')::interval
from seed_c sc
join public.lounge_posts p
  on p.title = sc.title and p.is_seed = true
where not exists (
  select 1 from public.lounge_comments c
   where c.post_id = p.id and c.content = sc.content
);

-- ── 4) comment_count 백필 (seed 글 + 기존 글 정합성 보정) ──────────────
update public.lounge_posts p
   set comment_count = (
     select count(*)
       from public.lounge_comments c
      where c.post_id = p.id
        and coalesce(c.is_deleted, false) = false
        and coalesce(c.is_hidden,  false) = false
   )
 where p.is_seed = true
    or exists (select 1 from public.lounge_comments c where c.post_id = p.id);
